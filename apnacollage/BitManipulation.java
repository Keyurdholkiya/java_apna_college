public class BitManipulation {
    public static void main(String[] args) {
        //Binary AND gate &
        System.out.println(5&6);
        //Binary OR gate |
        System.out.println(5|6);
        //Binary not gate
        System.out.println(~5);
        //Binary left shift <<
        System.out.println(5<<2);
        //Binary right shift >>
        System.out.println(5>>1);
    }
}
